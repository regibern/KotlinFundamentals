fun main (){
    println("\"Reginald Joselito P. Bernabe\"") //String
    println(13)
    println("13")
    println('L')

    var myName = "Reginald" //var is mutable
    val mySurname = "Bernabe" //val is immutable (can't be changed)
    println(myName)
    println(mySurname)

    //mySurname = "Penafiel" //will cause error because you can't change an immutable
    myName = "Riggo"
    println(myName)

    val myAge = 0
    println(myAge)
}
